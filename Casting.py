#Python Casting
#*Integers:
#Example:
x = int(8)
y = int(58.9)
z = int("8")
print(x)
print(y)
print(z)


#*Floats:
#Exmple:
x = float(25.5)
y = float(22.8)
z = float("8.4")
print(x)
print(y)
print(z)


#* Strings:
#Exmple:
x = str(10)
y = "Hello" + str(3) + "World"
z = str([1, 2, 3])
print(x)
print(y)
print(z)

