#Slicing Strings
#*Slicing
#Exmple:
x = "Hello, Friends!"
print(x[1:5]) #Output:ello,

#*Slice From the Start
# Example:
# Get the characters from the start to position 5 (not included):
x = "Hello, Friends!"
print(x[:5]) # Output: Hello


#*Slice To the End
#Exmple:
x = "Hello, Friends"
print(x[7:]) # Output: Friends


#*Negative Indexing
#Example:
x = "Hello, Friends!"
print(x[-5:-2])   #Output: end


